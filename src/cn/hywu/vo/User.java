package cn.hywu.vo;

public class User {
	private String id;
	private String name;
	private String password;
	private String careplace;
	private String careproduct;
	private String caremarket;
	private String caremonitor;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getCareplace() {
		return careplace;
	}
	public void setCareplace(String careplace) {
		this.careplace = careplace;
	}
	public String getCareproduct() {
		return careproduct;
	}
	public void setCareproduct(String careproduct) {
		this.careproduct = careproduct;
	}
	public String getCaremarket() {
		return caremarket;
	}
	public void setCaremarket(String caremarket) {
		this.caremarket = caremarket;
	}
	public String getCaremonitor() {
		return caremonitor;
	}
	public void setCaremonitor(String caremonitor) {
		this.caremonitor = caremonitor;
	}
	
}
